const choiceRandom = (choice) => {
    return choice[Math.floor(Math.random()*choice.length)]
}

const generateMatrix = () => {
    let input = []
    for (let i=0; i<8;i++){
        input[i] = choiceRandom([0,1,0])
    }

    return input
}

